package 정렬알고리즘;

import java.util.Scanner;

public class 알고리즘연습 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		System.out.print("n 입력 : ");
		
		int n = sc.nextInt();
		int sum = 0;
		
		for(int i=2;i<n;i+=2) {
		  
			sum += i;
		}System.out.println(sum);
		
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
		}
	







}
